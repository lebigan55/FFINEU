                                <h3 class="main-headline">U.S. stock market: reports are better than expected</h3>
                            </div>
                            <div class="overview-content">
                                <p>Timur Turlov, Chef of investment company</p>
                                <p>The US stock market: why is this earnings season much better than it seems at first glance</p>
                                <div class="overview-content-item overview-content-inner-1">
                                    <h6 class="main-headline">We expect</h6>
                                    <p> &#9830; positive tone in the last portion of reports</p>
                                    <p> &#9830; moderate growth for S&P 500 in May-June</p>
                                </div>
                                <div class="overview-content-item overview-content-inner-2">
                                    <h6 class="main-headline">Important news</h6>
                                    <p>&#9830; So far 450 companies from S&P 500 index have presented their financial reports for the last quarter. Most of them reported an increase in revenues and profits which exceeded market expectations. However, the total revenue and profit declined compared to the year ago.</p>
                                    <p>&#9830; The Federal Reserve kept its key interest rate unchanged on April 27. In its statement the U.S. central bank's rate-setting committee mentioned an improving labor market but acknowledged that economic growth seemed to have slowed but will return to the moderate pace in the near future. Fed signals about two rate hikes for this year.</p>
                                    <p>&#9830; There was so much data out in the United States from April 22 to May 5. GDP and the number of new jobs increased, but at weaker pace than expected. The most positive aspect was the growth in sales of cars and in orders for durable goods. This tells about a good condition of consumer demand in the United States.</p>
                                    <p>&#9830; On May 9 China's stock market lost nearly 3%, reaching eight-month low. Data on the country's foreign trade in April fell short of investor's hopes and signaled that the country's economy recovery is not at a steady pace: exports and imports fell more than expected.</p>
                                </div>
                                <div class="overview-content-item overview-content-inner-3">
                                    <p>After 450 companies from the S&P 500 index have reported their financial results we can confidently summarize US corporate activity in the first quarter: 54% of companies shown revenue growth, 58% increased their profits, but total revenue still declined by 2.4% and total profit by 8.68%. The results, at first glance, were significantly better than expected by analysts, but still weaker than at the same period a year ago. However, this season is in many ways stronger than the previous few ones, and I will try to explain - why. By the way, the market also saw this, and we are witnessing now lack of anxiety and willingness to grow further - and this is the most important thing.</p>
                                    <p>The most negative contribution to the results of Q1 was made by oil industry, basic materials and technology. Regarding first two sectors the reasons are clear - we passed through oil price lows and in Q2 this decline has been partly offset. In technologies (minus 3% in revenue and minus in 10% profit) the main negative impact came from Apple, which announced decrease in revenues and fall of iPhone sales. Furthermore, in the tech sector decline in revenue happened among a number of manufacturers of classic computer equipment - and it is quite expected change of trend.</p>
                                    <p>Nevertheless, tumble in those sectors will not stay forever, so the market will avoid negative pressure and this poor background will not become a hindrance for those who is able to rise today. And there are a lot of such companies on US market. We see an excellent revenue growth in the online segment (Google and Facebook in the first place), good results in healthcare and consumer sectors and communications. Yes, a few of the old corporations are slowing down overall growth, but consumer sentiment and innovative developments in the US are still growing – this means investors still have a space for making money.</p>
                                    <p>Bloomberg analysts have great expectations too. They expect a broad market growth: +1.5% in revenue and +2.5% in profits for the Q3 and +5% in revenue and +10% in profits for the Q4. This means that the number of leaders on the US stock market will show wonderful results with double-digit growth in profitability.</p>
                                    <p>The fact that the market sentiment remains positive is confirmed by IPO market recovery. In addition to the activization in a very promising biotech sector there are new «stars» like stock exchange BATS - investors liked it much and the listing went very well. In the context of general optimism and confidence we expect a very, very interesting IPOs in the future and good dynamics in many innovative industries.</p>
                                    <div class="overview-content-item-head">
                                        <h6>George Vaschenko, Head of trading / russian market</h6>
                                        <h6>Russian market: record highs expressed in rubles</h6>
                                    </div>
                                    <p>In Russia, we have the situation that reoccurs from eight years ago: the market has rewrote a historical high, however, without impairment of the ruble during this time. MICEX and RTS Indexes scored the top-10 by dynamics since the beginning of the year: the ruble index gained 8%, and its dollar counterpart – 22%. From February lows, which also became two-year lows, RTS Index soared 52%. It is the best result since 2009.</p>
                                    <p>Growth drivers for Russian market are an external context, oil prices, and dividend expectations as well. According to Igor Sechin, the head of Rosneft, OPEC actually sputtered out as it is today. Oil price approaches $50 for barrel against production and crude stockpile cuts in the USA, and in anticipation of a hot automobile season. The market got a boost from expectations of big dividends from state owned companies.</p>
                                    <p>The Central Bank, which did not lower a key rate in April, ascertained that the impact of its monetary policy weakens. Money market rates remain below the benchmark long time, and banks are building up an excessive ruble liquidity. As the economy is badly recovering, coupled with still weak demand for credits, there is a risk that in case of falling oil price, this money will begin to flow into foreign exchange market again. Preventing it, the Central Bank arranged sale of federal loan bond, taking away thus accumulated liquidity. The paradox of a situation here is that it could intervene the regulator’s plans to cut key rate, as the repo will become cheaper than bond yield. The similar phenomenon was observed in 2008, and in many respects intensified the consequences of the financial crisis, when the stock market started to correct.</p>
                                </div>
                                <div class="overview-content-item overview-content-inner-4">
                                    <h6 class="main-headline">Important news</h6>
                                    <p>&#9830; Gazprom has trouble with implementation of its investment program. The reason for that is growing number of defaults in domestic market, and collapsing profitability from export against dynamics of oil price. The planned volume of the monopoly’s expenses exceeds 1.5 trillion rubles for the current year.</p>
                                    <p>&#9830; Sberbank has increased profits under RAS by 2.9 times in 4 months comparing with the similar period of last year. Increase in interest income as volume of the issued credits is going up - became the driver of profit improvement. The share of mortgage loans in a portfolio exceeded 55%.</p>
                                    <p>&#9830; Rosneft entered international LNG market. The exact volume of the first shipment was not disclosed, but it could be about $15 million. It is sent to the Egyptian EGAS. Rosneft has strategic intentions to enter LNG market in Southeast Asia, including its own production.</p>                                   
                                </div>
                                <div class="overview-content-item overview-content-inner-5">
                                    <h6 class="main-headline">We expect</h6>
                                    <p>We do not exclude correction on the market with the target at 1810 points level of MICEX Index until the end of May. Correction of oil price and technical factors could trigger it. There is a number of first tier stock appear to be overbought, and traders are set to fix the long positions at reversal signals.</p>
                                    <h6>Our strategy</h6>
                                    <p>We consider stocks of Megafon, Dixi, Magnet for purchase on an average term with a potential growth of 10-15% by summer. We increase positions in ruble federal loan bonds with a duration to 3 years and the fixed income.</p>                                   
